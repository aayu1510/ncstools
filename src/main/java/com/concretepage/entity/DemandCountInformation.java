package com.concretepage.entity;

public class DemandCountInformation {
	
	private int totalDemands;
	private int totalHold;
	private int totalAbandoned;
	private int totalActive;
	public int getTotalDemands() {
		return totalDemands;
	}
	public void setTotalDemands(int totalDemands) {
		this.totalDemands = totalDemands;
	}
	public int getTotalHold() {
		return totalHold;
	}
	public void setTotalHold(int totalHold) {
		this.totalHold = totalHold;
	}
	public int getTotalAbandoned() {
		return totalAbandoned;
	}
	public void setTotalAbandoned(int totalAbandoned) {
		this.totalAbandoned = totalAbandoned;
	}
	public int getTotalActive() {
		return totalActive;
	}
	public void setTotalActive(int totalActive) {
		this.totalActive = totalActive;
	}
	
	

}
